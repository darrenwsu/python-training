"""
Nhap hai so a, b tu ban phim
+, -, *, /, //, **, %
"""
# input return string
a = float(input('a = '))
b = float(input('b = '))

print(a, '+', b, '=', a + b)
print(a, '-', b, '=', a - b)
print(a, '*', b, '=', a * b)
print(a, '/', b, '=', a / b)
print(a, '**', b, '=', a ** b)
print(a, '//', b, '=', a // b)
print(a, '%', b, '=', a % b)
